package com.neotech.review02;

public class WhileDemo {

	public static void main(String[] args) {
		
		String greeting="Miremengjes";
		
		int number=1;
		
		
		while (number <=5) {
			System.out.println(greeting);
			number++;
		}
		
		System.out.println("-----------------------");
		
		int count=5;
		
		while (count>=1) {
			System.out.println("Happy Mother's Day");
			count--;
		}
		
		
		System.out.println("-----------------------");

		int i=0;
		
		while (true) {
			
			if (i==2) {
				break;
			}
			
			System.out.println(greeting);
			i++;
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
